    <script>
        (function () {
            const form = document.getElementById('invite-form');
            const submit = document.getElementById('submit');
            const err = document.getElementById('err');
            const errSpan = err.querySelector('span');
            const token = @json($invitation->token);

            form.addEventListener('submit', async (e) => {
                e.preventDefault();
                err.style.display = 'none';
                submit.disabled = true;
                submit.querySelector('.label').textContent = 'Joining…';

                const payload = {
                    password: document.getElementById('password').value,
                };
                const nameField = document.getElementById('name');
                if (nameField) payload.name = nameField.value;

                try {
                    const res = await fetch('/api/invitations/' + token + '/accept', {
                        method: 'POST',
                        headers: { 'Content-Type': 'application/json', 'Accept': 'application/json' },
                        body: JSON.stringify(payload),
                    });
                    const data = await res.json();
                    if (!res.ok) {
                        const msg = data.errors
                            ? Object.values(data.errors).flat().join(' ')
                            : (data.message || 'Could not accept this invitation.');
                        throw new Error(msg);
                    }
                    // Redirect to the web admin so they can land in their new household.
                    window.location.href = '/admin';
                } catch (e) {
                    errSpan.textContent = e.message;
                    err.style.display = '';
                    submit.disabled = false;
                    submit.querySelector('.label').textContent = 'Accept & join';
                }
            });
        })();
    </script>
